package com.pansebastian.simpleparkour.objects;

public class ParkourPoint {

    public int X;
    public int Y;
    public int Z;
    public int position;

    public ParkourPoint(int X, int Y, int Z, int position)
    {
        this.X = X;
        this.Y = Y;
        this.Z = Z;
        this.position = position;
    }
}
